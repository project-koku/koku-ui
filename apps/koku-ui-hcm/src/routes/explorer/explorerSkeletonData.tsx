const getData = (name: string, values: number[], count: number) => {
  const datum = [];

  let index = 0;
  while (count > index) {
    datum.push({
      date: `${index}`,
      key: name,
      name,
      units: 'USD',
      x: `${index}`,
      y: values[index % values.length],
    });
    index++;
  }
  return datum;
};

